# 1. Выяснить тип результата выражений:
# 15 * 3
# 15 / 3
# 15 // 2
# 15 ** 2

# сразу видно, что одно выражение будет float - так как при делении в python в результате получится всегда float
# все остальные - int (при умножении/возведении в степень и целочисленного деления чисел типа int)

print(type(15 * 3))
print(type(15 / 3))
print(type(15 // 2))
print(type(15 ** 2))

